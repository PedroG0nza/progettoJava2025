package progettoJava2025;

public class calcolatrice 
{
	public int Somma (int val1 ,int val2) 
	{
	//scrivi il risultato
		int valore = val1+val2;
	return valore;
	}
	public int SommaNegativo(int va11, int val2) 
	{
		int valore = va11+val2;
		return valore;
	}
	public int Sott(int va11, int val2) 
	{
		int valore = va11-val2;
		return valore;
	}
}
